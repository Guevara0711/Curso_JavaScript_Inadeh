function suma(){
    var num 
    var resultado = 131

    num = prompt("¿Cuanto es la suma de 36 + 45 + 50?")
    if (num>='A' || num=='.'){
        alert("Ha escrito un caracter no válido. Solo se aceptan números")
    }else if(num == resultado){
        alert("La respuesta es correcta")
    }else{
        alert("La respuesta es incorrecta. La respuesta es: "+ resultado)
    }
}

